#import "DictBuilding.h"

@implementation DictBuilding
-(instancetype)initWithDict:(NSDictionary *)dict{
    if (self = [super init]) {

        self.ID = dict[@"id"];
        self.name = dict[@"name"];
    }
    return self;
}

+(instancetype)buildingWithDict:(NSDictionary*)dict{
    return [[self alloc] initWithDict:dict];
}

@end
